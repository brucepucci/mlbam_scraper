CREATE TABLE IF NOT EXISTS game (
    gid VARCHAR(255),
    venue VARCHAR(255),
    game_pk INTEGER,
    time_date VARCHAR(255),
    time_zone VARCHAR(255),
    ampm VARCHAR(255),
    first_pitch_et VARCHAR(255),
    away_time VARCHAR(255),
    away_time_zone VARCHAR(255),
    away_ampm VARCHAR(255),
    home_time VARCHAR(255),
    home_time_zone VARCHAR(255),
    home_ampm VARCHAR(255),
    venue_id INTEGER,
    away_name_abbrev VARCHAR(255),
    home_name_abbrev VARCHAR(255),
    away_code VARCHAR(255),
    away_team_id INTEGER,
    away_team_city VARCHAR(255),
    away_team_name VARCHAR(255),
    away_division VARCHAR(255),
    away_league_id INTEGER,
    home_code VARCHAR(255),
    home_team_id INTEGER,
    home_team_city VARCHAR(255),
    home_team_name VARCHAR(255),
    home_division VARCHAR(255),
    home_league_id INTEGER,
    day VARCHAR(255),
    double_header_sw VARCHAR(255),
    away_games_back FLOAT,
    home_games_back FLOAT,
    away_games_back_wildcard FLOAT,
    home_games_back_wildcard FLOAT,
    venue_w_chan_loc VARCHAR(255),
    away_win INTEGER,
    away_loss INTEGER,
    home_win INTEGER,
    home_loss INTEGER,
    league VARCHAR(255),
    away_team_runs INTEGER,
    home_team_runs INTEGER,
    away_team_hits INTEGER,
    home_team_hits INTEGER,
    away_team_errors INTEGER,
    home_team_errors INTEGER,
    PRIMARY KEY ( gid )
);
